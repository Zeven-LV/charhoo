package com.charhoo.build.prototype;

public class PrototypeShallow {
	
	private String name;
	
	public PrototypeShallow(){};
	
	public PrototypeShallow(String name){
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	};
	
	

}
